export function getInterfaceText(key, args = []) {
  let template = INTERFACE_TEXT[key] || key;
  args.forEach((arg, i) => {
    template = typeof template === "string"
      ? template.replace(new RegExp(`\\{${i}\\}`, "g"), arg ?? "")
      : template;
  });
  return template;
}
